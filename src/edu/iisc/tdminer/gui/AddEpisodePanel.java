    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonOkActionPerformed
    {//GEN-HEADEREND:event_jButtonOkActionPerformed
        try
        {
            switch(callerType)
            {
                case COUNTING_OTHERS:
                    prefs.put("episodeSet-COUNTING-OTHERS", jTextAreaEpisodes.getText());
                    break;
                case COUNTING_WITH_INTERVALS:
                    prefs.put("episodeSet-COUNTING-WITH-INTERVALS", jTextAreaEpisodes.getText());
                    break;
                case SIMULATOR:
                    prefs.put("episodeSet-SIMULATOR", jTextAreaEpisodes.getText());
                    break;
            }
        }catch(RuntimeException re)
        {
            re.printStackTrace();
        }

        cancelled = false;
        dialog.setVisible(false);
        dialog.dispose();        
    }//GEN-LAST:event_jButtonOkActionPerformed

