    public static List<IEpisode> getEpisode(EventFactor eventTypes, Frame frame, CallMode callerType, List<Interval> ivlList) 
        throws Exception
    {
        JDialog dialog = new JDialog(frame,"Event types selection", true);
        dialog.setSize(360,300);
        
        if (frame != null)
        {
            int x = (int)(frame.getLocation().getX() + (frame.getSize().getWidth() - dialog.getSize().getWidth())/2);
            int y = (int)(frame.getLocation().getY() + (frame.getSize().getHeight() - dialog.getSize().getHeight())/2);
            dialog.setLocation(x,y);
        }
        dialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
        AddEpisodePanel panel = new AddEpisodePanel(eventTypes, dialog, callerType, ivlList);
        dialog.getContentPane().add(panel);
        dialog.setVisible(true);
        return panel.getEpisode();
    }
    
    private AddEpisodePanel(EventFactor eventTypes, JDialog dialog, CallMode callerType, List<Interval> ivlList)
    {
        this.eventTypes = eventTypes;
        this.dialog = dialog;
        this.ivlList = ivlList;
        initComponents();    
        
        switch(callerType)
        {
            case COUNTING_OTHERS:
                jTextAreaEpisodes.setText( prefs.get("episodeSet-COUNTING-OTHERS","") );
                break;
            case COUNTING_WITH_INTERVALS:
                jTextAreaEpisodes.setText( prefs.get("episodeSet-COUNTING-WITH-INTERVALS","") );
                break;
            case SIMULATOR:
                jTextAreaEpisodes.setText( prefs.get("episodeSet-SIMULATOR","") );
                break;
        }
        this.callerType = callerType;
    }
    
    
    private List<IEpisode> getEpisode() throws Exception
    {
        List<IEpisode> eList = new ArrayList<IEpisode>();
        String[] lines = jTextAreaEpisodes.getText().split("\n");
        
        if (cancelled) return null;
        if (lines.length < 1) return null;
        
        for(String line : lines) {
            if (line.trim().length() < 1) continue;
            switch (this.callerType) {
                case COUNTING_OTHERS:
                    eList.add( Episode.getEpisode(line.trim(), eventTypes, ivlList) );
                    break;
                case COUNTING_WITH_INTERVALS:
                    if (SerialEpisodeParser.hasSerialEpisode(line))
                    {
                      System.out.println("Serial Episode with inter-event times found!!!");
                      eList.add(SerialEpisodeParser.getSerialEpisode(line, eventTypes, ivlList));
                    }
                    break;
                case SIMULATOR:
                    eList.add( ComboEpisode.getEpisode(line.trim(), eventTypes) );
                    break;
            }
        }
        return eList;
    }
    
