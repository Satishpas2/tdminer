    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed
// TODO add your handling code here:	
	try
	{
	    session.setIntervalExpiry(Double.parseDouble(jTextFieldIntervalExp.getText()));
	    session.setIntervalExpiryLow(Double.parseDouble(jTextFieldIntervalExpLow.getText()));
	    session.setEpisodeExpiry(Double.parseDouble(jTextFieldEpisodeExp.getText()));
            
            if (algos.get(jComboBoxAlgorithm.getSelectedIndex()) instanceof AlgoEntry)
            {
                this.counterIndex = jComboBoxAlgorithm.getSelectedIndex();
            }
	    dialog.setVisible(false);
	    dialog.dispose();
	}
	catch(NumberFormatException nfe)
	{
	    JOptionPane.showMessageDialog(this, "Invalid input : " + nfe.getMessage(), "Invalid Parameters", JOptionPane.ERROR_MESSAGE);
	}
    }//GEN-LAST:event_jButton1ActionPerformed
    
