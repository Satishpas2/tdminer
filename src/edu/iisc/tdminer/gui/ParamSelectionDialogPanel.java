    public ParamSelectionDialogPanel(JDialog dialog, SessionInfo session, int counterIndex, ArrayList algos)
    {
	initComponents();
	this.dialog = dialog;
	this.counterIndex = counterIndex;
	this.algos = algos;
        this.session = session;
        
	for (int i = 0; i < algos.size(); i++)
	{
	    jComboBoxAlgorithm.addItem(algos.get(i).toString());
	}
	jComboBoxAlgorithm.setSelectedIndex(counterIndex);

	NumberFormat nf = NumberFormat.getInstance();
	nf.setMaximumFractionDigits(8);
	
	double t = 0.0;
	t = session.getIntervalExpiry();
	jTextFieldIntervalExp.setText(nf.format(t));

	t = session.getIntervalExpiryLow();
	jTextFieldIntervalExpLow.setText(nf.format(t));

	t = session.getEpisodeExpiry();
	jTextFieldEpisodeExp.setText(nf.format(t));
	
	fitDefaultParameters();
    }
    
    public int getCounterIndex()
    {
	return counterIndex;
    }
    
public void fitDefaultParameters()
    {
        int index = jComboBoxAlgorithm.getSelectedIndex();
        Object objEntry = algos.get(index);
        if (objEntry instanceof AlgoEntry)
        {
            AlgoEntry entry = (AlgoEntry)objEntry;
            if (entry.isEpsExpiryEnable())
            {
                jTextFieldEpisodeExp.setEnabled(true);
                jLabelEpisodeExp.setEnabled(true);
            }
            else
            {
                jTextFieldEpisodeExp.setEnabled(false);
                jLabelEpisodeExp.setEnabled(false);
            }
            
            if (entry.isIvlHighEnable())
            {
                jTextFieldIntervalExp.setEnabled(true);
                jLabelIntervalExp.setEnabled(true);
            }
            else
            {
                jTextFieldIntervalExp.setEnabled(false);
                jLabelIntervalExp.setEnabled(false);
            }
            
            if (entry.isIvlLowEnable())
            {
                jLabelIntervalLow.setEnabled(true);
                jTextFieldIntervalExpLow.setEnabled(true);
            }
            else
            {
                jTextFieldIntervalExpLow.setText("0.0");
                jLabelIntervalLow.setEnabled(false);
                jTextFieldIntervalExpLow.setEnabled(false);
            }
        }
    }    
   
    
